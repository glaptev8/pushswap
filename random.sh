#!/bin/bash

MAXCOUNT=100
count=1

while [ "$count" -le $MAXCOUNT ]
do
	number=$(( ((RANDOM<<15)|RANDOM) % 63001 - 2000 ))
	echo -n $number ' '
	let "count += 1"
done
