#include "libft.h"

int		ft_stequally(char *str1, char *str2)
{
	while (*str1 != '\0' || *str2 != '\0')
	{
		if (*str1 == *str2)
		{
			str1++;
			str2++;
		}
		else
			return (-1);
	}
	return (1);
}
